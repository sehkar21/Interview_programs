
public class countofoddandeven {
public static void main(String[] args) {
	

int counteven=0;
int countodd=0;
for(int i=0;i<=100;i++)
{
	if(i%2==0) {
		counteven++;
	}else {
		countodd++;
	}
}
	System.out.println("the even number is "+counteven);
	System.out.println("the odd number is "+countodd);
	
	
}	
	
	
	
}
